def svm_loss(x, y):
    """
    Computes the loss and gradient using for multiclass SVM classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement loss and gradient for multiclass SVM classification.    #
    # This will be similar to the svm loss vectorized implementation in       #
    # cs231n/classifiers/linear_svm.py.                                       #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****

    num_train = x.shape[0]
    correct_class_score = x[range(num_train), list(y)].reshape(-1, 1)
    margin = np.maximum(0, x - correct_class_score + 1)
    margin[range(num_train), list(y)]=0
    loss = np.sum(margin)/num_train
    
    num_pos = np.sum(margin > 0, axis=1)
    dx = np.zeros_like(x)
    dx[margin > 0] = 1
    dx[np.arange(num_train), y] -= num_pos
    dx /= num_train
    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx


def softmax_loss(x, y):
    """
    Computes the loss and gradient for softmax classification.

    Inputs:
    - x: Input data, of shape (N, C) where x[i, j] is the score for the jth
      class for the ith input.
    - y: Vector of labels, of shape (N,) where y[i] is the label for x[i] and
      0 <= y[i] < C

    Returns a tuple of:
    - loss: Scalar giving the loss
    - dx: Gradient of the loss with respect to x
    """
    loss, dx = None, None
    ###########################################################################
    # TODO: Implement the loss and gradient for softmax classification. This  #
    # will be similar to the softmax loss vectorized implementation in        #
    # cs231n/classifiers/softmax.py.                                          #
    ###########################################################################
    # *****START OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****

    scores = np.copy(x)
    num_train = x.shape[0]
    correct_class_scores = scores[np.arange(num_train), y]
    max_scores = np.max(scores, axis=1, keepdims=True) #predicted scores
    scores -= max_scores #avoid large exp
    
    scores_exp = np.exp(scores)
    loss = max_scores.sum() - correct_class_scores.sum() + np.log(np.sum(scores_exp, axis=1)).sum()
    loss /= num_train
    
    dx = scores_exp/np.sum(scores_exp, axis=1, keepdims=True)
    dx[np.arange(num_train), y] -= 1
    
    dx /= num_train


    # *****END OF YOUR CODE (DO NOT DELETE/MODIFY THIS LINE)*****
    ###########################################################################
    #                             END OF YOUR CODE                            #
    ###########################################################################
    return loss, dx
